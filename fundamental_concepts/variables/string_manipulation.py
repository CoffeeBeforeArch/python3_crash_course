# Make a string variable
s = "Test string"

# Print the first 4 characters using a slice
print(s[0:4:1])

# Print the remainder of a string
print(s[5:])

# Split the spring into an array of strings
s = s.split(" ")
print(s)

# Combine the stings back together
s = " ".join(s)
print(s)
