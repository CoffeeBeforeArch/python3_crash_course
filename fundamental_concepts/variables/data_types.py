# Print the type of a string (str)
print(type("Hello, world!"))

# Print the type of an integer (int)
print(type(2))

# Print the type of a boolean (bool) ("False" also exists of course)
print(type(True))

# Prints the type of a floating point number (float)
print(type(4.3))
